class Window_Base
  #--------------------------------------------------------------------------
  # ● 開く処理の更新【※再定義※】
  #--------------------------------------------------------------------------
  def update_open
    self.openness += (281 - openness) / 3
    @opening = false if open?
  end
  #--------------------------------------------------------------------------
  # ● 閉じる処理の更新【※再定義※】
  #--------------------------------------------------------------------------
  def update_close
    self.openness -= (281 - openness) / 2
    @closing = false if close?
  end
end
